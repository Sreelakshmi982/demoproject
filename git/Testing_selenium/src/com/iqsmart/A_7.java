package com.iqsmart;

public class A_7 {
public static void main(String[] args) {
	int a =7;
	int b=9;
	System.out.println("before swapping"  );
	System.out.println(a );
	System.out.println(b );
	a=a+b;
	b=a-b;
	a=a-b;
	System.out.println("after swapping");
	
	System.out.println(a);
	System.out.println(b);
}
}
